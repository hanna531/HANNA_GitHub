    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

        File f = new File(jTextField1.getText());
        if(!f.isDirectory() && !f.isFile()){
            JOptionPane.showMessageDialog(this, "다시 입력해주세요.");
                   jTextField1.setText("");
                }
        else{   
        File[] abc = f.listFiles();
                for( File i : abc ){
            if(i.isDirectory()){
            jTextArea1.append("디렉토리: " + i.getPath()+i.getName()+"\n");
//                    System.out.println("디렉토리: " + i.getPath()+i.getName());
        }else if(i.isFile()){
            jTextArea1.append("파일: "+i.getPath()+i.getName()+"\n");
        }
                }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

